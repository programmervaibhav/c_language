#include<stdio.h>
int main(){
    
    int i=5;
    // pele print fir increment
    printf("The value after i++ is %d\n",i++);
    printf("The value after i is %d\n",i);
    // pele increment fir print
    printf("The value after ++i is %d\n",++i);

    // decrement similar to increment

        // pele print fir decrement

    printf("The value after i-- is %d\n",i--);
    printf("The value after i is %d\n",i);
        // pele decrement fir print

    printf("The value after --i is %d\n",--i);

    // += operator
    

    printf("The value of i is %d",i+=10);

    // 
    return 0;
}