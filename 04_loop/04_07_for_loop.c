#include<stdio.h>
int main(){
    // increment 
for(int a=0;a<10;a++){
    printf("The value of a is %d\n",a+1);
}

// quick quize
for (int n = 0; n <10; n++)
{
    printf("The values are %d\n",n+1);
}

// decrement

for (int j = 5; j; j--)
{
    printf("The values are %d\n",j);
}


    return 0;
}